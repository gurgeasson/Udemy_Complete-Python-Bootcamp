def add(number1, number2):
    """This function takes two numbers as argument and returns their sum."""
    return number1 + number2

def substract(number1, number2):
    """This function takes two numbers as argument and returns their difference"""
    return number1 - number2

def multiply(number1, number2):
    """This function takes two numbers as argument and returns their product"""
    return number1 * number2

def divide(number1, number2):
    """This function takes two numbers as argument and returns their quotient"""    
    return number1 / number2
